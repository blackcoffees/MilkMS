package com.cy.milkms.db.query;

public class ReportTotalNumberQuery {
	private int milkID;
	private String milkName;
	private int milkTotalNumber;
	private double milkTotalPrice;
	
	public int getMilkID() {
		return milkID;
	}
	public void setMilkID(int milkID) {
		this.milkID = milkID;
	}
	public String getMilkName() {
		return milkName;
	}
	public void setMilkName(String milkName) {
		this.milkName = milkName;
	}
	public int getMilkTotalNumber() {
		return milkTotalNumber;
	}
	public void setMilkTotalNumber(int milkTotalNumber) {
		this.milkTotalNumber = milkTotalNumber;
	}
	public double getMilkTotalPrice() {
		return milkTotalPrice;
	}
	public void setMilkTotalPrice(double milkTotalPrice) {
		this.milkTotalPrice = milkTotalPrice;
	}
	
	
}
